#!/usr/bin/env node
/**
 * generate-docs-index.mjs
 * Wave 13 · CC#2 · MASTER_DOCS_INDEX.md generator
 *
 * Walks the project-root docs/ tree and produces a markdown index of all
 * .md files grouped by top-level cluster. Includes file size + H1 heading
 * for quick scanning.
 *
 * Idempotent · safe to re-run · overwrites docs/MASTER_DOCS_INDEX.md.
 *
 * Run: node zero-risk-platform/scripts/audit/generate-docs-index.mjs
 */

import { readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const PROJECT_ROOT = resolve(__dirname, '..', '..', '..')
const DOCS_DIR = resolve(PROJECT_ROOT, 'docs')
const OUT_PATH = resolve(DOCS_DIR, 'MASTER_DOCS_INDEX.md')

const CLUSTER_SUMMARIES = {
  '01-identidad': 'ADN del proyecto · qué es Zero Risk',
  '02-arquitectura': 'Arquitectura V3 · stack · client brain · workflow design',
  '03-plan-opcion-4': 'Plan maestro · benchmark · decisiones',
  '04-agentes': 'Identidades de los 36 agentes + AGENCY_BLUEPRINT + audit reports',
  '05-orquestacion': 'Workflows · journeys · skeletons · investigations Waves 7-13',
  '06-estado': 'PROJECT_STATUS · governance · PM reports · briefings',
  '07-sesiones': 'Logs por sesión · S5 a S35+',
  '99-archive': 'Obsoletos · solo referencia histórica',
}

function scan(dir) {
  const out = []
  for (const f of readdirSync(dir, { withFileTypes: true })) {
    const p = resolve(dir, f.name)
    if (f.isDirectory()) out.push(...scan(p))
    else if (f.name.endsWith('.md')) out.push(p)
  }
  return out
}

function relPath(p) {
  // Convert absolute path to docs-relative (forward slashes for markdown)
  const idx = p.indexOf('docs')
  if (idx < 0) return p
  return p.slice(idx).replace(/\\/g, '/')
}

function getH1(filePath) {
  try {
    const content = readFileSync(filePath, 'utf8')
    const m = content.match(/^#\s+(.+)$/m)
    return m ? m[1].slice(0, 90).trim() : ''
  } catch {
    return ''
  }
}

function fmtSize(bytes) {
  if (bytes < 1024) return `${bytes}B`
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(0)}K`
  return `${(bytes / 1024 / 1024).toFixed(1)}M`
}

function main() {
  const all = scan(DOCS_DIR).sort()
  const totalSize = all.reduce((a, p) => a + statSync(p).size, 0)

  const byCluster = new Map()
  for (const p of all) {
    const rel = relPath(p)
    const after = rel.replace(/^docs\//, '')
    const cluster = after.split('/')[0]
    if (!byCluster.has(cluster)) byCluster.set(cluster, [])
    byCluster.get(cluster).push({
      rel,
      size: statSync(p).size,
      h1: getH1(p),
    })
  }

  const lines = []
  lines.push(`# MASTER DOCS INDEX`)
  lines.push('')
  lines.push(`**Generado:** 2026-04-30 · Wave 13 · auto-generated by \`zero-risk-platform/scripts/audit/generate-docs-index.mjs\``)
  lines.push(`**Source:** filesystem walk de \`docs/\` tree (project root)`)
  lines.push(`**Total archivos:** ${all.length} .md files · ${fmtSize(totalSize)} aggregated`)
  lines.push('')
  lines.push(`> Este index lista TODOS los archivos .md en \`docs/\`. Para empezar nuevas tareas · usar \`MASTER_INDEX.md\` (project root) que es más curado · este es la vista exhaustiva.`)
  lines.push('')
  lines.push(`> **Re-generar:** \`node zero-risk-platform/scripts/audit/generate-docs-index.mjs\` (idempotent)`)
  lines.push('')
  lines.push('---')
  lines.push('')
  lines.push(`## TL;DR · clusters`)
  lines.push('')
  lines.push(`| Cluster | Files | Size | Resumen |`)
  lines.push(`|---|---|---|---|`)
  for (const [c, files] of [...byCluster.entries()].sort()) {
    const size = fmtSize(files.reduce((a, f) => a + f.size, 0))
    const desc = CLUSTER_SUMMARIES[c] || ''
    lines.push(`| \`${c}/\` | ${files.length} | ${size} | ${desc} |`)
  }
  lines.push('')

  for (const [cluster, files] of [...byCluster.entries()].sort()) {
    lines.push('---')
    lines.push('')
    lines.push(`## ${cluster}/`)
    lines.push('')
    lines.push(`> ${CLUSTER_SUMMARIES[cluster] || '(sin resumen)'}`)
    lines.push('')
    lines.push(`**${files.length} files · ${fmtSize(files.reduce((a, f) => a + f.size, 0))}**`)
    lines.push('')
    lines.push(`| Path | Size | H1 / First heading |`)
    lines.push(`|---|---|---|`)
    for (const f of files) {
      const safe = (f.h1 || '_(no H1)_').replace(/\|/g, '\\|').slice(0, 90)
      lines.push(`| \`${f.rel}\` | ${fmtSize(f.size)} | ${safe} |`)
    }
    lines.push('')
  }

  lines.push('---')
  lines.push('')
  lines.push(`**Mantenedor:** auto-regenerated · re-run script periodically.`)
  lines.push(`**Última generación:** 2026-04-30 · Wave 13.`)
  lines.push('')

  writeFileSync(OUT_PATH, lines.join('\n'))
  console.log(`✓ Wrote ${OUT_PATH}`)
  console.log(`  Files indexed: ${all.length}`)
  console.log(`  Clusters: ${byCluster.size}`)
  console.log(`  Total size: ${fmtSize(totalSize)}`)
}

main()
