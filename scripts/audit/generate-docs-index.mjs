#!/usr/bin/env node
/**
 * generate-docs-index.mjs
 * Wave 13/15 · auto-generates docs/MASTER_DOCS_INDEX.md
 *
 * Walks the docs/ tree (project root, sibling of zero-risk-platform/)
 * and produces a clustered index of every .md file with size and first H1.
 *
 * Usage:
 *   node zero-risk-platform/scripts/audit/generate-docs-index.mjs
 *   node zero-risk-platform/scripts/audit/generate-docs-index.mjs --dry-run
 *
 * Idempotent: writes the same output for the same docs tree.
 */

import { readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs'
import { resolve, dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, '..', '..')
const PROJECT_ROOT = resolve(REPO_ROOT, '..')
const DOCS_ROOT = resolve(PROJECT_ROOT, 'docs')
const OUTPUT = resolve(DOCS_ROOT, 'MASTER_DOCS_INDEX.md')

const args = process.argv.slice(2)
const DRY = args.includes('--dry-run')

const CLUSTER_DESCRIPTIONS = {
  '01-identidad': 'ADN del proyecto · qué es Zero Risk',
  '02-arquitectura': 'Arquitectura V3 · stack · client brain · workflow design',
  '03-plan-opcion-4': 'Plan maestro · benchmark · decisiones',
  '04-agentes': 'Identidades de los 36 agentes + AGENCY_BLUEPRINT + audit reports',
  '05-orquestacion': 'Workflows · journeys · skeletons · investigations Waves 7-13',
  '06-estado': 'PROJECT_STATUS · governance · PM reports · briefings',
  '07-sesiones': 'Logs por sesión · S5 a S35+ (Phase 1 archive cleanup ejecutado 2026-05-01)',
  '99-archive': 'Obsoletos · solo referencia histórica',
}

function walk(dir) {
  const out = []
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name)
    if (entry.isDirectory()) {
      out.push(...walk(full))
    } else if (entry.isFile() && entry.name.endsWith('.md')) {
      out.push(full)
    }
  }
  return out
}

function firstH1(content) {
  for (const line of content.split(/\r?\n/)) {
    const m = line.match(/^#\s+(.+?)\s*$/)
    if (m) return m[1].trim()
  }
  return ''
}

function humanSize(bytes) {
  if (bytes < 1024) return `${bytes}B`
  if (bytes < 1024 * 1024) return `${Math.round(bytes / 1024)}K`
  return `${(bytes / 1024 / 1024).toFixed(1)}M`
}

function topCluster(relPath) {
  const parts = relPath.split(/[\\/]/)
  return parts[0]
}

const allFiles = walk(DOCS_ROOT)
const byCluster = new Map()
let totalBytes = 0

for (const file of allFiles) {
  const rel = relative(DOCS_ROOT, file).replace(/\\/g, '/')
  if (rel === 'MASTER_DOCS_INDEX.md') continue
  const content = readFileSync(file, 'utf8')
  const size = statSync(file).size
  totalBytes += size
  const cluster = topCluster(rel)
  const isRoot = !rel.includes('/')
  const key = isRoot ? '__root__' : cluster
  if (!byCluster.has(key)) byCluster.set(key, [])
  byCluster.get(key).push({
    path: `docs/${rel}`,
    size,
    h1: firstH1(content),
  })
}

const clusterOrder = [
  '__root__',
  '01-identidad',
  '02-arquitectura',
  '03-plan-opcion-4',
  '04-agentes',
  '05-orquestacion',
  '06-estado',
  '07-sesiones',
  '99-archive',
]

let md = ''
md += '# MASTER DOCS INDEX\n\n'
md += `**Generado:** ${new Date().toISOString().slice(0, 10)} · auto-generated by \`zero-risk-platform/scripts/audit/generate-docs-index.mjs\`\n`
md += `**Source:** filesystem walk de \`docs/\` tree (project root)\n`
md += `**Total archivos:** ${allFiles.length - 1} .md files · ${humanSize(totalBytes)} aggregated\n\n`
md += '> Este index lista TODOS los archivos .md en `docs/`. Para empezar nuevas tareas · usar `MASTER_INDEX.md` (project root) que es más curado · este es la vista exhaustiva.\n\n'
md += '> **Re-generar:** `node zero-risk-platform/scripts/audit/generate-docs-index.mjs` (idempotent)\n\n'
md += '---\n\n## TL;DR · clusters\n\n'
md += '| Cluster | Files | Size | Resumen |\n|---|---|---|---|\n'

for (const key of clusterOrder) {
  if (!byCluster.has(key)) continue
  const list = byCluster.get(key)
  if (key === '__root__') {
    const sum = list.reduce((a, b) => a + b.size, 0)
    md += `| (root) | ${list.length} | ${humanSize(sum)} | Indexes y planes raíz |\n`
    continue
  }
  const sum = list.reduce((a, b) => a + b.size, 0)
  md += `| \`${key}/\` | ${list.length} | ${humanSize(sum)} | ${CLUSTER_DESCRIPTIONS[key] || ''} |\n`
}
md += '\n---\n\n'

for (const key of clusterOrder) {
  if (!byCluster.has(key)) continue
  const list = byCluster.get(key).sort((a, b) => a.path.localeCompare(b.path))
  if (key === '__root__') {
    md += `## (root indexes)\n\n`
  } else {
    md += `## ${key}/\n\n> ${CLUSTER_DESCRIPTIONS[key] || ''}\n\n`
  }
  const sum = list.reduce((a, b) => a + b.size, 0)
  md += `**${list.length} files · ${humanSize(sum)}**\n\n`
  md += '| Path | Size | H1 / First heading |\n|---|---|---|\n'
  for (const f of list) {
    const h1 = (f.h1 || '').replace(/\|/g, '\\|')
    md += `| \`${f.path}\` | ${humanSize(f.size)} | ${h1} |\n`
  }
  md += '\n---\n\n'
}

md += `_End of auto-generated index._\n`

if (DRY) {
  console.log(md)
  console.log(`\n[dry-run] would write ${OUTPUT}`)
} else {
  writeFileSync(OUTPUT, md)
  console.log(`Wrote ${OUTPUT} · ${allFiles.length - 1} files indexed across ${[...byCluster.keys()].length} clusters · ${humanSize(totalBytes)} total`)
}
