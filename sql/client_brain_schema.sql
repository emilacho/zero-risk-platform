-- Search brand books by semantic similarity
CREATE OR REPLACE FUNCTION search_brand_books(
  p_client_id UUID,
  p_query_embedding vector(3072),
  p_match_count INTEGER DEFAULT 3
)
RETURNS TABLE (
  id UUID,
  content_text TEXT,
  similarity FLOAT
)
LANGUAGE plpgsql
AS $$
BEGIN
  RETURN QUERY
  SELECT
    bb.id,
    bb.content_text,
    1 - (bb.embedding <=> p_query_embedding) AS similarity
  FROM client_brand_books bb
  WHERE bb.client_id = p_client_id
    AND bb.embedding IS NOT NULL
  ORDER BY bb.embedding <=> p_query_embedding
  LIMIT p_match_count;
END;
$$;

