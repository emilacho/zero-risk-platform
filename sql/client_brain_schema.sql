-- ============================================================
-- Step 10: Unified Client Brain search
-- Single function that searches ALL brain sections at once
-- Returns top-K most relevant chunks from across all tables
-- ============================================================
CREATE OR REPLACE FUNCTION query_client_brain(
  p_client_id UUID,
  p_query_embedding vector(3072),
  p_sections TEXT[] DEFAULT ARRAY['brand_books', 'icp_documents', 'voc_library', 'competitive_landscape', 'historical_outputs'],
  p_match_count INTEGER DEFAULT 10
)
RETURNS TABLE (
  source_table TEXT,
  source_id UUID,
  label TEXT,
  content_text TEXT,
  similarity FLOAT
)
LANGUAGE plpgsql STABLE SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  WITH all_results AS (
    -- Brand Books
    SELECT
      'brand_books'::TEXT AS source_table,
      bb.id AS source_id,
      ('Brand Book v' || bb.version)::TEXT AS label,
      bb.content_text,
      1 - (bb.embedding <=> p_query_embedding) AS similarity
    FROM client_brand_books bb
    WHERE bb.client_id = p_client_id
      AND bb.embedding IS NOT NULL
      AND 'brand_books' = ANY(p_sections)

    UNION ALL

    -- ICP Documents
    SELECT
      'icp_documents'::TEXT,
      icp.id,
      ('ICP: ' || icp.audience_segment)::TEXT,
      icp.content_text,
      1 - (icp.embedding <=> p_query_embedding)
    FROM client_icp_documents icp
    WHERE icp.client_id = p_client_id
      AND icp.embedding IS NOT NULL
      AND 'icp_documents' = ANY(p_sections)

    UNION ALL

    -- VOC Library
    SELECT
      'voc_library'::TEXT,
      v.id,
      ('VOC [' || v.sentiment || ']: ' || v.source)::TEXT,
      v.content_text,
      1 - (v.embedding <=> p_query_embedding)
    FROM client_voc_library v
    WHERE v.client_id = p_client_id
      AND v.embedding IS NOT NULL
      AND 'voc_library' = ANY(p_sections)

    UNION ALL

    -- Competitive Landscape
    SELECT
      'competitive_landscape'::TEXT,
      cl.id,
      ('Competitor: ' || cl.competitor_name)::TEXT,
      cl.content_text,
      1 - (cl.embedding <=> p_query_embedding)
    FROM client_competitive_landscape cl
    WHERE cl.client_id = p_client_id
      AND cl.embedding IS NOT NULL
      AND 'competitive_landscape' = ANY(p_sections)

    UNION ALL

    -- Historical Outputs
    SELECT
      'historical_outputs'::TEXT,
      ho.id,
      (ho.output_type || ': ' || ho.title)::TEXT,
      ho.content_text,
      1 - (ho.embedding <=> p_query_embedding)
    FROM client_historical_outputs ho
    WHERE ho.client_id = p_client_id
      AND ho.embedding IS NOT NULL
      AND 'historical_outputs' = ANY(p_sections)
  )
  SELECT ar.source_table, ar.source_id, ar.label, ar.content_text, ar.similarity
  FROM all_results ar
  ORDER BY ar.similarity DESC
  LIMIT p_match_count;
END;
$$;

-- ============================================================
-- Step 11: Get pre-generation guardrails for a client
-- Returns forbidden words + required terminology for injection
-- ============================================================
CREATE OR REPLACE FUNCTION get_client_guardrails(p_client_id UUID)
RETURNS TABLE (
  forbidden_words JSONB,
  required_terminology JSONB,
  voice_description TEXT,
  competitor_mentions_policy TEXT,
  compliance_notes TEXT
)
LANGUAGE plpgsql
AS $$
BEGIN
  RETURN QUERY
  SELECT
    bb.forbidden_words,
    bb.required_terminology,
    bb.voice_description,
    bb.competitor_mentions_policy,
    bb.compliance_notes
  FROM client_brand_books bb
  WHERE bb.client_id = p_client_id
    AND bb.human_validated = true
  ORDER BY bb.version DESC
  LIMIT 1;
END;
$$;

