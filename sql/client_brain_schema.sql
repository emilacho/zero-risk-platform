-- ============================================================
-- Step 0: Enable pgvector extension
-- ============================================================
CREATE EXTENSION IF NOT EXISTS vector;

-- ============================================================
-- Step 1: Clients master table
-- Every Client Brain table references clients.id
-- ============================================================
CREATE TABLE IF NOT EXISTS clients (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  slug TEXT NOT NULL UNIQUE,
  website_url TEXT,
  industry TEXT,
  market TEXT,                           -- e.g. "B2B SaaS", "B2C Retail", "Professional Services"
  status TEXT NOT NULL DEFAULT 'onboarding'
    CHECK (status IN ('onboarding', 'active', 'paused', 'churned')),
  preferred_language TEXT NOT NULL DEFAULT 'es', -- output language
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Index for quick slug lookups
CREATE INDEX IF NOT EXISTS idx_clients_slug ON clients(slug);

-- ============================================================
-- Step 2: client_brand_books
-- Voice, tone, positioning, forbidden words, approved phrases
-- ============================================================
CREATE TABLE IF NOT EXISTS client_brand_books (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  client_id UUID NOT NULL REFERENCES clients(id) ON DELETE CASCADE,

  -- Brand Foundation
  brand_purpose TEXT,                    -- Why the brand exists
  brand_vision TEXT,                     -- Where the brand is going
  brand_mission TEXT,                    -- How it gets there
  brand_values JSONB DEFAULT '[]',       -- Array of values with descriptions
  brand_personality TEXT,                -- e.g. "Bold, approachable, expert"

  -- Voice & Tone
  voice_description TEXT,                -- How the brand sounds
  tone_guidelines JSONB DEFAULT '{}',    -- Tone variations by context (formal, casual, crisis)
  writing_style TEXT,                    -- Style preferences (short sentences, active voice, etc.)

  -- Messaging Architecture
  tagline TEXT,
  elevator_pitch TEXT,
  key_messages JSONB DEFAULT '[]',       -- Array of primary messages
  value_propositions JSONB DEFAULT '[]', -- Array of value props

  -- Visual Identity (references, not files)
  primary_colors JSONB DEFAULT '[]',     -- Hex codes
  typography JSONB DEFAULT '{}',         -- Font families + usage rules
  imagery_style TEXT,                    -- "Bright, people-focused, no stock photos"
  logo_usage_notes TEXT,

  -- Guardrails (Pre-generation constraints)
  forbidden_words JSONB DEFAULT '[]',    -- Words/phrases NEVER to use
  required_terminology JSONB DEFAULT '[]', -- Terms ALWAYS to use when relevant
  competitor_mentions_policy TEXT DEFAULT 'never_mention',
    -- CHECK (competitor_mentions_policy IN ('never_mention', 'compare_favorably', 'acknowledge_neutral'))
  compliance_notes TEXT,                 -- Industry-specific compliance (e.g. "No health claims without citation")

  -- Auto-generation tracking
  auto_generated BOOLEAN NOT NULL DEFAULT false, -- true = generated by Brand Strategist scraping
  auto_generated_from TEXT,              -- URL used for auto-generation
  human_validated BOOLEAN NOT NULL DEFAULT false, -- true = human reviewed and approved v0
  version INTEGER NOT NULL DEFAULT 1,

  -- RAG embedding
  content_text TEXT,                     -- Concatenated searchable text for embedding
  embedding vector(3072),               -- Voyage-3 or Claude embedding

  -- Metadata
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_brand_books_client ON client_brand_books(client_id);

-- ============================================================
-- Step 3: client_icp_documents
-- Ideal Customer Profiles — supports N segments per client
-- ============================================================
CREATE TABLE IF NOT EXISTS client_icp_documents (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  client_id UUID NOT NULL REFERENCES clients(id) ON DELETE CASCADE,

  -- Segment Identity
  audience_segment TEXT NOT NULL,        -- e.g. "Enterprise CTO", "SMB Marketing Director"
  segment_priority INTEGER NOT NULL DEFAULT 1, -- 1 = primary, 2 = secondary, etc.

  -- Demographics
  job_titles JSONB DEFAULT '[]',         -- Array of typical job titles
  company_size TEXT,                     -- e.g. "50-200 employees"
  industries JSONB DEFAULT '[]',         -- Array of industries this segment is in
  geography TEXT,                        -- e.g. "North America", "Global", "LATAM"

  -- Psychographics & JTBD
  goals JSONB DEFAULT '[]',              -- What they're trying to achieve
  pain_points JSONB DEFAULT '[]',        -- What frustrates them
  jobs_to_be_done JSONB DEFAULT '[]',    -- JTBD framework entries
  objections JSONB DEFAULT '[]',         -- Common objections to the client's product/service

  -- Behavior
  information_sources JSONB DEFAULT '[]', -- Where they get info (LinkedIn, podcasts, conferences)
  buying_process TEXT,                   -- How they make purchase decisions
  decision_criteria JSONB DEFAULT '[]',  -- What matters most in their decision
  budget_range TEXT,                     -- Typical budget for this segment

  -- Messaging
  key_messages_for_segment JSONB DEFAULT '[]', -- Tailored messages for this segment
  preferred_channels JSONB DEFAULT '[]',       -- Best channels to reach them
  content_preferences TEXT,              -- e.g. "Long-form case studies, not video"

  -- RAG embedding
  content_text TEXT,
  embedding vector(3072),

  -- Metadata
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_icp_client ON client_icp_documents(client_id);
CREATE INDEX IF NOT EXISTS idx_icp_segment ON client_icp_documents(client_id, audience_segment);

-- ============================================================
-- Step 4: client_voc_library
-- Voice of Customer — real quotes + metadata
-- ============================================================
CREATE TABLE IF NOT EXISTS client_voc_library (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  client_id UUID NOT NULL REFERENCES clients(id) ON DELETE CASCADE,

  -- Quote
  quote_text TEXT NOT NULL,              -- The actual customer quote
  source TEXT NOT NULL,                  -- Where it came from: "Google Review", "NPS Survey", "Interview", "Social Media"
  source_url TEXT,                       -- Link to original if available
  customer_name TEXT,                    -- Anonymous if not available
  customer_segment TEXT,                 -- Maps to audience_segment in ICP

  -- Classification
  sentiment TEXT NOT NULL DEFAULT 'positive'
    CHECK (sentiment IN ('positive', 'negative', 'neutral')),
  category TEXT,                         -- e.g. "product_quality", "customer_service", "pricing", "ease_of_use"
  themes JSONB DEFAULT '[]',             -- Array of themes this quote relates to

  -- Usage tracking
  used_in_content BOOLEAN NOT NULL DEFAULT false, -- Has this quote been used in agency content?
  last_used_at TIMESTAMPTZ,
  usage_count INTEGER NOT NULL DEFAULT 0,

  -- RAG embedding
  content_text TEXT,                     -- = quote_text + context for richer embedding
  embedding vector(3072),

  -- Metadata
  quote_date DATE,                       -- When the customer said this
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_voc_client ON client_voc_library(client_id);
CREATE INDEX IF NOT EXISTS idx_voc_sentiment ON client_voc_library(client_id, sentiment);

